const path = require('path');
const fs = require('fs');
const os = require('os');

let configPath = '';
let dataDir = '';
let config = {};

// Detect if running inside a packaged pkg environment
const isPackaged = typeof process.pkg !== 'undefined';

function generateConfigContent(detected) {
    const formatValue = (val) => {
        if (Array.isArray(val)) {
            return '[\n' + val.map(p => `        ${JSON.stringify(p)}`).join(',\n') + '\n    ]';
        }
        return JSON.stringify(val);
    };

    return `// ============================================================================
// MEMO-RAY CONFIGURATION CONTRACT
// ============================================================================
// This configuration was auto-generated by the Memo-Ray setup detector.
// You can edit these paths below, or configure them using the Mission Control dashboard.
// ============================================================================

module.exports = {
    // Directory where Claude Code stores active session PIDs
    CLAUDE_SESSIONS_DIR: ${formatValue(detected.CLAUDE_SESSIONS_DIR.value)},

    // Directories where Claude Code stores conversational logs (.jsonl)
    CLAUDE_LOG_DIRS: ${formatValue(detected.CLAUDE_LOG_DIRS.value)},

    // Path to the file that tracks Git Worktrees created by Claude Code
    CLAUDE_WORKTREES_PATH: ${formatValue(detected.CLAUDE_WORKTREES_PATH.value)},

    // Path to the Claude Desktop application configuration (for tool discovery)
    CLAUDE_CONFIG_PATH: ${formatValue(detected.CLAUDE_CONFIG_PATH.value)},

    // Directories where Antigravity stores its conversation trajectories (.jsonl)
    ANTIGRAVITY_BRAIN_DIRS: ${formatValue(detected.ANTIGRAVITY_BRAIN_DIRS.value)},

    // Directory for Gemini Agent plugins and core configuration
    GEMINI_CONFIG_DIR: ${formatValue(detected.GEMINI_CONFIG_DIR.value)}
};
`;
}

// Configuration and database always live in the user's home folder (~/.memoray)
const configDir = path.join(os.homedir(), '.memoray');
if (!fs.existsSync(configDir)) {
    fs.mkdirSync(configDir, { recursive: true });
}

configPath = path.join(configDir, 'memoray.config.js');
dataDir = path.join(configDir, 'data');

// If config file doesn't exist, create it dynamically using the detector
if (!fs.existsSync(configPath)) {
    try {
        const { detectPaths } = require('./detector');
        const detected = detectPaths().results;
        const configContent = generateConfigContent(detected);
        fs.writeFileSync(configPath, configContent, 'utf8');
    } catch (err) {
        console.error('[Config] Failed to generate default config:', err.message);
    }
}

// Make sure the data directory exists
if (!fs.existsSync(dataDir)) {
    fs.mkdirSync(dataDir, { recursive: true });
}

const vm = require('vm');

function reloadConfig() {
    try {
        if (fs.existsSync(configPath)) {
            const content = fs.readFileSync(configPath, 'utf8');
            const sandbox = {
                module: { exports: {} },
                require: require,
                path: path,
                os: os,
                console: console
            };
            vm.runInNewContext(content, sandbox);

            // Mutate the existing config object in-place so references in other modules remain valid
            for (const key of Object.keys(config)) {
                delete config[key];
            }
            Object.assign(config, sandbox.module.exports);
        }
    } catch (e) {
        console.error(`[Config] Failed to load configuration from ${configPath}:`, e.message);
    }
}

// Initial load
reloadConfig();

module.exports = {
    config,
    configPath,
    dataDir,
    isPackaged,
    reloadConfig
};
